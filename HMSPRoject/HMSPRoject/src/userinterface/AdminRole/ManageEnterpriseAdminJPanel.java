    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) ManageSysAdminJTable.getModel();

        model.setRowCount(0);
        for (UserAccount userAccount : ecosystem.getUserDir().getUserAccountsList()) {
                    Object[] row = new Object[2];
                    row[0] = userAccount.getUserName();
                    row[1] = userAccount.getRole();

                    model.addRow(row);
                }
    }
    
    private void populateEnterpriseComboBox(){
        enterpriseNameComboBox.removeAllItems();
        
        for (Enterprise enterprise : ecosystem.getNetworkList().get(0).getEnterpriseDirectory().getEnterpriseList()){
            enterpriseNameComboBox.addItem(enterprise);
        }
        
    }
