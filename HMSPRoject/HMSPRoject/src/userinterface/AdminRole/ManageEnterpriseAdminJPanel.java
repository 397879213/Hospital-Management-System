    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        Enterprise enterprise = (Enterprise) enterpriseNameComboBox.getSelectedItem();
        
        String username = usernameTxtField.getText();
        String password = String.valueOf(passwordTxtField1.getText());
        
        Employee employee = enterprise.getEmpDir().createEmployee(username);
        //UserAccount account = ecosystem.getUserDir().createUserAccount(username, password, employee, new Admin());
        if(enterprise.getEnterpriseType()==Enterprise.EnterpriseType.Hospital)
        {
            UserAccount account = ecosystem.getUserDir().createUserAccount(username, password, employee, new SystemAdmin());
        }
        else
        {
           UserAccount account = ecosystem.getUserDir().createUserAccount(username, password, employee, new MedicalEquipmentSupplier()); 
        }
        populateTable();
    }//GEN-LAST:event_createBtnActionPerformed

